
public class OperadoresDeAsingnacionEnJava {

    public static void main(String args[]) {
        int a = 3, b = 5;
        int c = 6, d = 2;
        System.out.println("d = " + d);
        
        //operador autoincrement
        
        a += 1;// a = a + 1;
        System.out.println("a = " + a);//a se incrementa en 1
        
        a += 5;
        System.out.println("a = " + a);
        
        //resta
        a -= 2;
        System.out.println("a = " + a);
        
        //multiplicacion
        a *= 7;
        System.out.println("a = " + a);
        
        //divicion 
        a /= 7;
        System.out.println("a = " + a);
        
        //modulo 
        a %= 2;
        System.out.println("a = " + a);
    }
}
