
public class ConcatenacionJava {

    public static void main(String args[]) {
        var usuario = "Juan";
        var titulo = " Ingeniero ";
        var unirVaribales = usuario + titulo;
        System.out.println("unirVaribales = " + unirVaribales);
        
        var numero1 = 40;
        var numero2 = 45;
        var unionNumeros = "La suma entre: "+ numero1 + " y " +numero2+ " es: " + (numero1 + numero2);
        System.out.println(unionNumeros);

    }
}
